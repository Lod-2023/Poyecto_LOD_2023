CREATE VIEW vista_productos_con_categoria AS
SELECT p.codigo_producto, p.nombre_producto, p.lote_producto, p.cantidad, c.nombre AS categoria
FROM producto p
INNER JOIN categoria c ON p.id_categoria = c.id_categoria;

