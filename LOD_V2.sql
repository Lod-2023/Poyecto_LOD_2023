CREATE VIEW vista_casos_con_detalles AS
SELECT
    c.id_caso,
    c.fecha,
    c.codigo_set,
    u.nombre AS nombre_usuario,
    u.apellido AS apellido_usuario,
    p.id_paciente,
    p.nombre AS nombre_paciente,
    p.apellido AS apellido_paciente,
    ubic.nombre_ubicacion
FROM caso c
INNER JOIN usuario u ON c.id_usuario = u.id_usuario
INNER JOIN paciente p ON c.id_paciente = p.id_paciente
INNER JOIN ubicacion ubic ON c.id_ubicacion = ubic.id_ubicacion;


SELECT * FROM vista_casos_con_detalles


CREATE VIEW vista_plantillas_con_detalles AS
