codigo_producto varchar (50) primary key not null,
nombre_producto varchar (100) not null,
lote_producto varchar (50) not null,
cantidad int not null,
id_categoria int not null,
foreign key (id_categoria) references categoria(id_categoria)

)

create table plantilla (
codigo_set varchar(50) not null,
codigo_producto varchar (50) not null,
nombre varchar (100) not null,
lote varchar (50) not null,
existencia int not null,
plantilla int not null,
faltante int not null,
foreign key (codigo_set) references set_(codigo_set),
foreign key (codigo_producto) references producto(codigo_producto)
)

